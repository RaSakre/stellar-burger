describe('ingredientsConstructorSlice', () => {
  let store: any;

  beforeEach(() => {
    store = configureStore({
      reducer: {
        constructorReducer: reducer
      }
    });
  });
  test('addBun test', () => {
    const action = addBun(bunMock);
    const newState = reducer(
      {
        ...initialState,
        ingredientsConstructor: { bun: bunMock, ingredients: [] }
      },
      action
    );
    expect(newState.ingredientsConstructor.bun).toEqual(bunMock);
  });
  test('addIngredients test', () => {
    const action = addIngredients(ingredientsMock);
    const newState = reducer(
      {
        ...initialState,
        ingredientsConstructor: { bun: emptyBun, ingredients: [] }
      },
      action
    );
    expect(newState.ingredientsConstructor.ingredients).toEqual([
      ingredientsMock
    ]);
  });
  test('deleteIngredient test', () => {
    const ingr = ingredientsMock[0];
    const action = deleteIngredient({ _id: ingr._id });
    const newState = reducer(
      {
        ...initialState,
        ingredientsConstructor: { bun: emptyBun, ingredients: ingredientsMock }
      },
      action
    );
    expect(newState.ingredientsConstructor.ingredients).toEqual(
      editedIngredients
    );
  });
  test('moveIngredient test', () => {
    const action = moveIngredient({ fromIndex: 0, toIndex: 1 });
    const newState = reducer(
      {
        ...initialState,
        ingredientsConstructor: { bun: emptyBun, ingredients: ingredientsMock }
      },
      action
    );
    expect(newState.ingredientsConstructor.ingredients).toEqual(
      sortedIngredients
    );
  });
  test('clearIngredients test', () => {
    const action = clearConstructor();
    const newState = reducer(
      {
        ...initialState,
        ingredientsConstructor: { bun: bunMock, ingredients: ingredientsMock }
      },
      action
    );
    expect(newState).toEqual({
      ...initialState,
      ingredientsConstructor: { bun: emptyBun, ingredients: [] }
    });
  });
});
